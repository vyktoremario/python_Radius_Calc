print ('Area of a Circle Calculator!!')

# Area of a cirlce is PI*Radius*Radius, PI is constant
PI=3.14

#Let user enter the radius value
radius = float(input(' Please Enter the radius of the circle: '))

# calculating the Area of circle, using the formular
area= PI * radius * radius

# Printing the value of Area
print('Area: %.2f' %area)
